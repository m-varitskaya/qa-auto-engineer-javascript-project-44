import readlineSync from 'readline-sync';
import startG from './brain-games.js';
import { name } from '../src/cli.js';

startG();

console.log('Answer "yes" if the number is even, otherwise answer "no".');

function isEven(number) {
    return number % 2 === 0 ? `yes` : `no`;
};

function startGame () {
    
    for (let n = 0; n < 3; n++) {
        var getRandomIntInclusive = Math.floor(Math.random() * 20);
        let fuuu = isEven(getRandomIntInclusive);
        console.log(`Question: ${getRandomIntInclusive}`);
        const userAnswer = readlineSync.question('Your answer: ');
            if (userAnswer === fuuu && n < 2) {
            console.log(`Correct!`);
            }
            else if (userAnswer === fuuu && n === 2) {
              console.log(`Congratulations, ${name}`);
              return;
            }
            else {
                console.log(`'${userAnswer}' is wrong answer ;(. Correct answer was '${fuuu}'.`);
                return;
          }
    }
};
        

startGame();

